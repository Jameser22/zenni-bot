import random

QUOTES = [
    {"text": "Логика — это анатомия мышления.", "author": "Джон Локк"},
    {"text": "Сомнение — начало мудрости.", "author": "Аристотель"},
    {"text": "Думать — самая трудная работа; вот, вероятно, почему этим занимаются столь немногие.", "author": "Генри Форд"},
    {"text": "Ум заключается не только в знании, но и в умении применять знание на деле.", "author": "Аристотель"},
    {"text": "Мозг, хорошо устроенный, стоит больше, чем мозг, хорошо наполненный.", "author": "Мишель де Монтень"},
]

def get_random_quote() -> str:
    quote = random.choice(QUOTES)
    return f"«{quote['text']}»\n— {quote['author']}"